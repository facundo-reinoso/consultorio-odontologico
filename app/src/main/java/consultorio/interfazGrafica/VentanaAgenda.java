    private void botonModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonModificarMouseClicked
        try{
            Object o1 = comboBoxOdontologos.getSelectedItem();
       
            Odontologo odontologoElegido = (Odontologo) o1;
            
            IntAgenda agenda = odontologoElegido.getAgendaPersonal();
               
            Integer documento = Integer.parseInt(campoDocumento.getText());
            Integer dia = Integer.parseInt(campoDia.getText());
            Integer mes = Integer.parseInt(campoMes.getText());
            Integer año = Integer.parseInt(campoAño.getText());
            Integer hora = Integer.parseInt(campoHora.getText());
            Integer minutos = Integer.parseInt(campoMinuto.getText());
            String motivo = campoMotivo.getText();
            Boolean dobleTurno = rbDobleTurno.isSelected();
                
            agenda.modificarTurno(documento, LocalDate.of(año, mes, dia), LocalTime.of(hora, minutos), motivo, dobleTurno);
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Se cargaron mal los datos.");
        }catch(TurnoNoExisteException e){
            JOptionPane.showMessageDialog(null, "El turno buscado no existe.");
        }catch(TurnoNoCargadoException e){
            JOptionPane.showMessageDialog(null, "Verifique la fecha y los horarios.");
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error.");
        }
        
        campoDia.setText("");
        campoMes.setText("");
        campoAño.setText("");
        campoHora.setText("");
        campoMinuto.setText("");
        campoMotivo.setText("");
        rbDobleTurno.setSelected(false);
        
    }//GEN-LAST:event_botonModificarMouseClicked

