    private void botonBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonBuscarMouseClicked
        try{
            Object o = comboBoxOdontologos.getSelectedItem();
        
            if(o instanceof Odontologo){
                Odontologo odontologo = (Odontologo) o;
            
                IntAgenda agenda = odontologo.getAgendaPersonal();
            
                List<Turno> turnoEncontrado = new ArrayList<>();
            
                turnoEncontrado.add(agenda.buscarTurno(Integer.parseInt(campoDocumento.getText())));
            
                DefaultTableModel datos = (DefaultTableModel) tablaTurnos.getModel();
        
                datos.setNumRows(0);
        
                for(Turno i: turnoEncontrado){
            
                    Object[] fila = { i.getPaciente().toString(), i.getFecha(), i.getHoraInicio(), i.getHoraFin(), i.getMotivo()};
                    datos.addRow(fila);
                }
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Se cargaron mal los datos.");
        }catch(TurnoNoExisteException e){
            JOptionPane.showMessageDialog(null, "El turno no existe.");
        }catch(RuntimeException e){
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error.");
        }
        
       
    }//GEN-LAST:event_botonBuscarMouseClicked

    private void botonMostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonMostrarMouseClicked
        Object o = comboBoxOdontologos.getSelectedItem();
        
        if(o instanceof Odontologo){
            Odontologo odontologo = (Odontologo) o;
            
            IntAgenda agenda = odontologo.getAgendaPersonal();
            
            List<Turno> turnos = agenda.mostrarTurnos();
            
            DefaultTableModel datos = (DefaultTableModel) tablaTurnos.getModel();
        
            datos.setNumRows(0);
        
            for(Turno i: turnos){
            
                Object[] fila = { i.getPaciente().toString(), i.getFecha(), i.getHoraInicio(), i.getHoraFin(), i.getMotivo()};
                datos.addRow(fila);
            }
        }
        
    }//GEN-LAST:event_botonMostrarMouseClicked

